#include "miniRT.h"

uint8_t	ft_vec3_is_normalized(t_vec3 u)
{
	if (u.x < -1.0F || u.y < -1.0F || u.z < -1.0F
		|| u.x > 1.0F || u.y > 1.0F || u.z > 1.0F)
		return (ft_pstderr(__ERR_10), 0);
	return (1);
}
