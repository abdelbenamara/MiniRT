#include "miniRT.h"

t_vec3	ft_vec3_cross(t_vec3 u, t_vec3 v)
{
	return (ft_vec3(\
		u.y * v.z - u.z * v.y, \
		u.z * v.x - u.x * v.z, \
		u.x * v.y - u.y * v.x));
}
